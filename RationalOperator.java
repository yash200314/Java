public class RationalOperator {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println("logical and");
        if(a && b)
        {
            System.out.println(true);
        }
        else
        System.out.println(false);


        boolean a1 = true;
        boolean b1 = false;

        System.out.println("logical Or");
        // logical or evalutes to true if one value is true;
        if(a1 || b1)
        {
            System.out.println(true);
        }
        else
        System.out.println(false);



        System.out.println("logical Or");
        // logical not  evalutes to true if one value is true;
    }
}
